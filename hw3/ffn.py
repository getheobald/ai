# used same values from the lab but can come back and adjust if accuracy isn't right
# input layer must start with 784 inputs because the input images are 28*28 pixels
# output layer must output 10 outputs because there are 10 classes
class FF_Net(nn.Module):
    def __init__(self):
        super().__init__()
        self.flatten = nn.Flatten()
        self.linear_relu_stack = nn.Sequential(
            nn.Linear(28*28, 512),
            nn.ReLU(),
            nn.Linear(512, 512),
            nn.ReLU(),
            nn.Linear(512, 10)
        )

    def forward(self, x):
        x = self.flatten(x)
        logits = self.linear_relu_stack(x)
        return logits